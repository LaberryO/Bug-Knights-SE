# 색깔 가져다 쓰는 용도

class Color:
    def black(self):
        return (0, 0, 0);

    def white(self):
        return (255, 255, 255);

    def gray(self):
        return (128, 128, 128);

    def customYellow(self):
        return (255, 199, 30);

    def customGreen(self):
        return (168, 255, 108);
        
